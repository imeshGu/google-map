function initMap() {
    const map = new google.maps.Map(document.getElementById("map"), {
        zoom: 15, // Set your desired zoom level
        center: { lat: gpsData[0].lat, lng: gpsData[0].lng }, // Set the initial map center
        mapId: '50b734b33f9a860d'
    });

    // Create an array to hold LatLng objects
    const routeCoordinates = [];

    // Process GPS data and convert it to LatLng objects
    gpsData.forEach((point) => {
        const latLng = new google.maps.LatLng(point.lat, point.lng);
        routeCoordinates.push(latLng);
    });

    // Create a Polyline to display the route on the map
    const routePath = new google.maps.Polyline({
        path: routeCoordinates,
        geodesic: true,
        strokeColor: "#FF0000", // Line color
        strokeOpacity: 1.0,
        strokeWeight: 2, // Line width
    });

    // Set the Polyline on the map 
    routePath.setMap(map);
}



